
class ImageMetadata:
    def __init__(self=None, title=None, ra_center=None, dec_center=None, naxes=None, naxis=None, scale=None, format=None, image_url=None):
        self.title = title
        self.ra_center = ra_center
        self.dec_center = dec_center
        self.naxes = naxes
        self.naxis = naxis
        self.scale = scale
        self.format = format
        self.image_url = image_url

